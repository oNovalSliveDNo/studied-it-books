# Забавные строки
# Демонстрирует сцепление и повторение строк

# Сцепление (или "склеивание") двух строк с помощью оператора +
print("Две строки можно " + "сцепить с помощью оператора '+'.")

# Пример длинной строки, собранной из множества маленьких строк с помощью +
# Обратный слэш (\) позволяет переносить длинную команду на новую строку
# \n внутри строки делает перевод строки в выводе
print("\nЭта строка " + "вряд ли " + "удив" + "ит вас. " \
      + "Но интересно, " + "что" + " она - \n" + "одна длинная-длин" \
      + "на" + "я" + " строка, сцепленная " \
      + "из " + "двадцати двух\n" + "отдельных строк, разбитых " \
      + "на шесть строк." + " Достаточно ли вы" + " впечатлены? " + "Ну? хватит,\n" \
      + "вот " + "наша " + "длинная" + " строка и закончилась!")

# Пример повторения строки — оператор * позволяет повторить одну и ту же строку много раз
print("\nЕсли текст нравится, его можно и повторить. К примеру,")
print("кто не любит пирожные? Только дураки не любят пирожные. А")
print("раз мы люди честные, то выскажемся о своих симпатиях")
print("откровенно:")
print("Пирожные" * 10)  # Эта строка выведется 10 раз подряд

# Ожидаем, пока пользователь нажмёт Enter, чтобы программа не закрылась сразу
input("\n\nНажмите Enter, чтобы выйти.")
