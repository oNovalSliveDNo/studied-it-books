# Game Over - версия 2
# Показывает разные приемы работы со строками

# Выводит заголовок программы
print("Программа 'Game Over' 2.0")

# Выводит несколько строк подряд через запятую — они автоматически разделяются пробелами
print("То же", "самое", "сообщение")

# То же самое, но строки написаны на разных строках кода — Python всё равно объединяет их
print("Только",
      "чуть-чуть",
      "побольше")

# Выводит два слова в одной строке, но двумя вызовами print
# end=" " отменяет перевод строки после первого print и вставляет пробел вместо него
print("Вот", end=" ")
print("оно...")

# Многострочная строка в тройных кавычках (""" """)
# Она сохраняет все переносы строк и пробелы — удобно для вывода "артов" и больших текстов
print(
    """
      _____       ___       ___  ___   _____
     / ____|     /   |     /   |/   | |  ___|
     | |        / /| |    / /|   /| | | |__
     | |  _    / ___ |   / / |__/ | | |  __|
     | |_| |  / /  | |  / /       | | | |___
     \\_____/ /_/   |_| /_/        |_| |_____|

      _____   _     _   _____   _____
     /  _  \\ | |   / / |  ___| |  _  \\
     | | | | | |  / /  | |__   | |_| |
     | | | | | | / /   |  __|  |  _  /
     | |_| | | |/ /    | |___  | | \\ \\
     \\_____/ |___/     |_____| |_|  \\_\\
    """
)

# Ожидает нажатия Enter, чтобы программа не закрылась сразу
input("\n\nНажмите Enter, чтобы выйти.")
