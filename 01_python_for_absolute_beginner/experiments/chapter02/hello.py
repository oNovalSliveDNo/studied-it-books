# Привет
# Демонстрирует использование переменных

# Создаём переменную с именем name и присваиваем ей значение "Vasya"
name = "Vasya"

# Выводим значение переменной name (в данном случае — строку "Vasya")
print(name)

# Выводим строку "Hello," и значение переменной name, всё на одной строке
# Результат будет: Hello, Vasya
print("Hello,", name)

