# Персональный привет
# Демонстрирует получение пользовательского ввода

# Показывает сообщение и ждёт, пока пользователь введёт своё имя с клавиатуры
# Введённый текст сохраняется в переменной name
name = input("Привет. Как тебя зовут? ")

# Выводит значение переменной name (имя, введённое пользователем)
print(name)

# Приветствие с использованием введённого имени
print("Привет,", name)

# Ждём, пока пользователь нажмёт Enter, чтобы окно не закрылось сразу
input("\n\nHажмите Enter, чтобы выйти.")
