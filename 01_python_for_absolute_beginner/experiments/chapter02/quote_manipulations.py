# Манипуляции с цитатой
# Демонстрирует строковые методы

# цитата из Томаса Уотсона, который в 1943 г. был директором IBM
# строка сохраняется в переменную quote
quote = "Думаю, на мировом рынке можно будет продать штук пять компьютеров."

# Выводим исходную цитату
print("Исходная цитата: ")
print(quote)

# Преобразуем всю строку в верхний регистр (заглавные буквы)
print("\n0на же в верхнем регистре:")
print(quote.upper())

# Преобразуем всю строку в нижний регистр (строчные буквы)
print("\nB нижнем регистре: ")
print(quote.lower())

# Делаем каждое слово в строке с заглавной буквы (как в заголовках)
print("\nКак заголовок: ")
print(quote.title())

# Заменяем часть строки: "штук пять" → "несколько миллионов"
# Метод replace не изменяет оригинальную строку, а создаёт новую
print("\nC ма-а-аленькой заменой:")
print(quote.replace("штук пять", "несколько миллионов"))

# Показываем, что оригинальная строка не изменилась — она осталась такой же, как была
print("\nA вот опять исходная цитата:")
print(quote)

