# Демонстрирует type conversion

# Многострочный текст — описание программы, выводится целиком
print(
    """
                    Рантье
    Программа подсчитывает ваши ежемесячные расходы. Эту статистику нужно 
    знать, чтобы у вас не закончились деньги и вам не пришлось искать работу.
    
    Введите суммы расходов по всем статьям, перечисляемым ниже. Вы богаты - 
    так не мелочитесь, пишите суммы в долларах, без центов.
    """
)

# Сначала получаем строку от пользователя (input), потом превращаем её в целое число (int)
car = input("Техническое обслуживание машины 'Ламборгини': ")
car = int(car)  # Преобразуем строку в число

# Здесь сразу преобразуем ввод в целое число — без промежуточной переменной
rent = int(input("Съем роскошной квартиры на Манхэттене: "))
get = int(input("Аренда самолета: "))
gifts = int(input("Подарки: "))
food = int(input("Обеды и ужины в ресторанах: "))
staff = int(input("Жалованье прислуги (дворецкий, повар, водитель, секретарь): "))
guru = int(input("Плата личному психоаналитику: "))
games = int(input("Компьютерные игры: "))

# Складываем все числовые значения расходов
total = car + rent + get + gifts + food + staff + guru + games

# Выводим итоговую сумму расходов
print("\nобщая сумма:", total)

# Ждём нажатия Enter, чтобы программа не закрылась сразу
input("\n\nНажмите Enter, чтобы выйти.")
