# Воображаемые благодарности
# Образец применения escape-последовательностей

# \t — символ табуляции (отступ), используется для выравнивания текста
print("\t\t\tВоображаемые благодарности")

# \\ — экранированный обратный слэш. Один слэш отображается как символ, а не как спец. последовательность
print("\t\t\t \\ \\ \\ \\ \\ \\ \\")

print("\t\t\tразработчика игры")
print("\t\t\tМайкла Доусона")
print("\t\t\t \\ \\ \\ \\ \\ \\ \\")

# \n — переход на новую строку
print("\nОтдельное спасибо хотелось бы сказать:")

# \' — экранирование одинарной кавычки, чтобы она не конфликтовала с кавычками строки
# \" — экранирование двойных кавычек
print("моему парикмахеру Генри по прозвищу \'Великолепный\', который не знает слова \"невозможно\".")

# \a — специальная escape-последовательность, которая вызывает звук системного динамика (если он поддерживается)
print("\a")

# Ждём нажатия Enter, чтобы окно не закрылось сразу
input("\n\nНажмите Enter, чтобы выйти.")
