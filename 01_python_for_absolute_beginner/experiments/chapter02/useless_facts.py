# Бесполезные факты
#
# Узнает у пользователя его / ее личные данные и выдает несколько фактов
# о нем / ней. Эти факты истинны, но совершенно бесполезны.

# Получаем имя пользователя
name = input("Привет. Как тебя зовут? ")

# Получаем возраст, но сразу преобразуем введённое значение в число (int), чтобы работать с ним как с числом
age = input("Сколько тебе лет? ")
age = int(age)  # Преобразуем строку в целое число

# Получаем вес пользователя и также преобразуем в число
weight = int(input("Хорошо, и последний вопрос. Сколько в тебе килограммов? "))

# Переводим имя в нижний регистр и выводим, как если бы поэт Каммингс писал письмо
print("\nЕсли бы поэт Каммингс адресовал тебе письмо, он бы обратился к тебе так:", name.lower())

# Переводим имя в верхний регистр и выводим, как если бы это был "рехнувшийся" Каммингс
print("А если бы это был рехнувшийся Каммингс, то так:", name.upper())

# Умножаем имя на 5, чтобы показать, как ребёнок может повторять имя
called = name * 5
print("\nЕсли бы маленький ребёнок решил привлечь твоё внимание,")
print("он произнёс бы твоё имя так:")
print(called)

# Считаем, сколько секунд прожито, исходя из возраста пользователя (переводим годы в секунды)
seconds = age * 365 * 24 * 60 * 60  # 365 дней, 24 часа, 60 минут, 60 секунд
print("\nТвой нынешний возраст - свыше", seconds, "секунд.")

# Пример того, сколько бы весил человек на Луне (меньше, чем на Земле)
moon_weight = weight / 6  # Вес на Луне примерно в 6 раз меньше
print("\nЗнаете ли вы, что на луне вы весили бы всего", moon_weight, "кг?")

# Пример того, сколько бы весил человек на Солнце (тоже гипотетически, потому что это невозможно)
sun_weight = weight * 27.1  # Вес на Солнце в 27.1 раза больше, чем на Земле
print("А вот находясь на Солнце, вы бы весили", sun_weight, "кг (Но, увы, это продолжалось бы недолго...)")

# Ждём, пока пользователь нажмёт Enter, чтобы не закрылась сразу
input("\n\nНажмите Enter, чтобы выйти. ")
