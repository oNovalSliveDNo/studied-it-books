# Анаграммы (Word Jumble)
#
# Компьютер выбирает какое-либо слово и хаотически переставляет его буквы
# Задача игрока - восстановить исходное слово

import random

# создаем последовательность слов, из которых компьютер будет выбирать одно
WORDS = ("питон", "анаграмма", "простая", "сложная", "ответ")

# случайным образом выбираем одно слово из списка
word = random.choice(WORDS)

# сохраняем правильное слово для сравнения с ответом игрока
correct = word

# создаем анаграмму выбранного слова, перемешивая его буквы случайным образом
jumble = ""
while word:
    # выбираем случайную позицию в слове
    position = random.randrange(len(word))
    # добавляем букву из выбранной позиции в анаграмму
    jumble += word[position]
    # удаляем выбранную букву из исходного слова
    word = word[:position] + word[(position + 1):]

# выводим приветствие и объяснение для игрока
print("""
            Добро пожаловать в игру 'Анаграммы'!
        Надо переставить буквы так, чтобы получилось осмысленное слово.
    (Для выхода нажмите Enter, не вводя своей версии.)
""")
# показываем игроку анаграмму (перемешанные буквы)
print("Вот анаграмма: ", jumble)

# запрашиваем у игрока его вариант ответа
guess = input("\nПопробуйте отгадать исходное слово: ")
# продолжаем запрашивать ответ, пока он не совпадет с правильным или игрок не решит выйти
while guess != correct and guess != "":
    print("К сожалению, вы неправы.")  # если ответ неправильный
    guess = input("Попробуйте отгадать исходное слово: ")

# если игрок угадал правильное слово
if guess == correct:
    print("Да, именно так! Вы отгадали!\n")

# выводим благодарность за игру
print("Спасибо за игру.")

# ожидаем нажатия Enter для завершения программы
input("\n\nНажмите Enter, чтобы выйти.")
