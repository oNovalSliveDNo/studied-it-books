# Переводчик с гикского на русский
# Демонстрирует использование словарей

# создаем словарь с терминами (ключ — термин, значение — его перевод/объяснение)
geek = {
    '404': 'Не знать, не владеть информацией. От сообщения об ошибке 404 "Страница не найдена"',
    'Googling': '"Гугление", поиск в Сети сведений о ком-либо',
    'Keyboard Plaque': 'Мусор, который скапливается в клавиатуре компьютера',
    'Link Rot': 'Процесс устаревания гиперссылок на веб-страницах',
    'Percussive Maintenance': 'О ситуации, когда кто-либо бьет по корпусу неисправного электронного прибора в надежде восстановить его работу',
    'Uninstalled': 'Об увольнении кого-либо. Особенно популярно на рубеже 1990-2000-х годов'
}

choice = None  # переменная для хранения выбора пользователя из меню

# цикл меню — повторяется, пока пользователь не выберет "0"
while choice != "0":

    # отображаем меню
    print(
        """
        Переводчик с гикского на русский
    
        0 - Выйти
        1 - Найти перевод термина
        2 - Добавить новый термин
        3 - Изменить существующий термин
        4 - Удалить термин
        """
    )

    choice = input("Ваш выбор: ")  # получаем выбор пользователя
    print()

    # если пользователь выбрал "0" — выходим из программы
    if choice == "0":
        print("До свидания!")

    # если пользователь хочет найти перевод термина
    elif choice == "1":
        term = input("Kaкoй термин вы хотите перевести с гикского на русский? ")
        if term in geek:  # если термин есть в словаре
            definition = geek[term]  # получаем перевод
            print("\n", term, "означает:", definition)
        else:  # если термин не найден
            print("\nУвы, этот термин мне незнаком: ", term)

    # если пользователь хочет добавить новый термин
    elif choice == "2":
        term = input("Kaкoй термин гикского языка вы хотите добавить?: ")
        if term not in geek:  # проверяем, нет ли такого термина уже в словаре
            definition = input("\nВпишите ваше толкование: ")  # просим ввести значение
            geek[term] = definition  # добавляем термин в словарь
            print("\nТермин", term, "добавлен в словарь.")
        else:  # если термин уже есть
            print("\nТакой термин уже есть! Попробуйте изменить его толкование.")

    # если пользователь хочет изменить определение существующего термина
    elif choice == "3":
        term = input("Какой термин вы хотите переопределить?: ")
        if term in geek:  # если термин существует
            definition = input("Впишите ваше толкование: ")  # просим новое определение
            geek[term] = definition  # обновляем значение в словаре
            print("\n", term, "обновлён.")
        else:  # если термин не найден
            print("\nТакого термина пока нет! Попробуйте добавить его в словарь.")

    # если пользователь хочет удалить термин
    elif choice == "4":
        term = input("Какой термин вы хотите удалить?: ")
        if term in geek:  # если термин есть в словаре
            del geek[term]  # удаляем его
            print("\nТермин", term, "удалён.")
        else:  # если термина нет
            print("\nНичем не могу помочь. Термина", term, "нет в словаре.")

    # если пользователь ввел недопустимый пункт меню
    else:
        print("\nИзвините, в меню нет пункта", choice)

    # ждём, пока пользователь нажмёт Enter, чтобы завершить программу
input("\n\nНажмите Enter, чтобы выйти.")
