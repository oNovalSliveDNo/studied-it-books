# Рекорды
# Демонстрирует списочные методы

scores = []  # создаем пустой список для хранения рекордов
choice = None  # переменная для хранения выбора пользователя

# основной цикл программы — продолжается, пока пользователь не введёт '0'
while choice != '0':

    # выводим меню с вариантами действий
    print(
        """
        0 - Выйти
        1 - Показать рекорды
        2 - Добавить рекорд
        3 - Удалить рекорд
        4 - Сортировать список
        """)
    choice = input("Ваш выбор: ")  # получаем ввод пользователя

    print()  # выводим пустую строку для красоты

    # выход из программы
    if choice == '0':
        print("До свидания")

    # вывод всех текущих рекордов
    elif choice == '1':
        print("Рекорды: ")
        for score in scores:  # перебираем каждый рекорд в списке
            print(score)  # выводим рекорд

    # добавление нового рекорда в список
    elif choice == '2':
        score = int(input('Впишите свой рекорд: '))  # запрашиваем число у пользователя
        scores.append(score)  # добавляем это число в список

    # удаление указанного рекорда из списка
    elif choice == '3':
        score = int(input('Какой из рекордов удалить?: '))  # запрашиваем, какой рекорд удалить
        if score in scores:  # проверяем, есть ли такой рекорд в списке
            scores.remove(score)  # удаляем рекорд
        else:
            print('Результат', score, 'не содержится в списке рекордов')  # если нет — сообщаем об этом

    # сортировка списка по убыванию (от большего к меньшему)
    elif choice == '4':
        scores.sort(reverse=True)  # сортируем список в обратном порядке

    # если пользователь ввёл что-то не из меню
    else:
        print('Извините, в меню нет пункта', choice)

# ждем нажатия Enter перед выходом
input("\n\nНажмите Enter, чтобы выйти. ")
