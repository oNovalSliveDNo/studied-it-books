# Рекорды
# Демонстрирует вложенные последовательности

scores = []  # создаем пустой список, в котором будут храниться рекорды (каждый как кортеж: (результат, имя))
choice = None  # переменная для хранения выбора пользователя из меню

# основной цикл программы, работает до тех пор, пока пользователь не выберет '0'
while choice != '0':

    # выводим меню
    print(
        """
        0 - Выйти
        1 - Показать рекорды
        2 - Добавить рекорд
        """)

    choice = input("Ваш выбор: ")  # получаем выбор пользователя

    print()  # просто пустая строка для лучшей читаемости

    # если пользователь выбрал выход
    if choice == '0':
        print("До свидания")

    # если пользователь выбрал просмотр рекордов
    elif choice == '1':
        print("Рекорды\n")
        print("ИМЯ\tРЕЗУЛЬТАТ")  # выводим заголовок таблицы
        for entry in scores:  # проходим по всем элементам (рекордам) в списке
            score, name = entry  # распаковываем кортеж на результат и имя
            print(name, '\t', score)  # выводим имя и результат через табуляцию

    # если пользователь выбрал добавление нового рекорда
    elif choice == '2':
        name = input('Впишите имя игрока: ')  # запрашиваем имя
        score = int(input('Впишите его результат: '))  # запрашиваем числовой результат
        entry = (score, name)  # создаем кортеж из результата и имени
        scores.append(entry)  # добавляем кортеж в список рекордов
        scores.sort(reverse=True)  # сортируем список по убыванию (лучшие результаты вверху)
        scores = scores[:5]  # оставляем только 5 лучших записей в списке

    # если введено что-то, чего нет в меню
    else:
        print('Извините, в меню нет пункта', choice)

# ожидаем нажатия Enter перед завершением программы
input("\n\nНажмите Enter, чтобы выйти. ")
