# День рождения
# Демонстрирует именованные аргументы и значения параметров по умолчанию

# позиционные параметры
# Эта функция принимает два параметра: имя и возраст, и выводит поздравление
def birthday1(name, age):
    print("С днём рождения,", name, "!", "Вам сегодня исполняется", age, ", не так ли?\n")


# параметры со значениями по умолчанию
# Эта функция тоже принимает имя и возраст, но если их не указать,
# будут использоваться значения по умолчанию: "товарищ Иванов" и 1
def birthday2(name="товарищ Иванов", age=1):
    print("С днем рождения,", name, "!", "Вам сегодня исполняется", age, ", не так ли?\n")


# Вызов функции с позиционными аргументами (в порядке: имя, возраст)
birthday1("товарищ Иванов", 1)

# Вызов функции с аргументами в обратном порядке — работает, но логически неверно
birthday1(1, "товарищ Иванов")

# Вызов с именованными аргументами — можно менять порядок
birthday1(name="товарищ Иванов", age=1)
birthday1(age=1, name="товарищ Иванов")

# Вызовы функции birthday2 со значениями по умолчанию

# Вызов без аргументов — используются значения по умолчанию
birthday2()

# Указано только имя, возраст берётся по умолчанию
birthday2(name="Катя")

# Указан только возраст, имя берётся по умолчанию
birthday2(age=12)

# Указаны оба аргумента — переопределяют значения по умолчанию
birthday2(name="Катя", age=12)

# Тоже указаны оба аргумента, но позиционно (в правильном порядке)
birthday2("Катя", 12)

