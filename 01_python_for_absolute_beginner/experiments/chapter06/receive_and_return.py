# Принимай - возвращай
# Демонстрирует параметры и возвращаемые значения

# Эта функция принимает один параметр (сообщение) и просто выводит его на экран
def display(message):
    print(message)


# Эта функция ничего не принимает, но возвращает число 5
def give_me_five():
    five = 5
    return five


# Эта функция задаёт пользователю вопрос с возможными ответами "y" или "n"
# и возвращает тот ответ, который ввёл пользователь (в нижнем регистре)
def ask_yes_no(question):
    """Задает вопрос с ответом 'да' или 'нет '."""

    response = None  # Переменная для хранения ответа пользователя
    # Повторять запрос, пока не будет введено "y" или "n"
    while response not in ("y", "n"):
        response = input(question).lower()  # Приводим ответ к нижнему регистру
    return response  # Возвращаем корректный ответ


# основная часть

# Вызов функции display(), передаём ей строку — она выведет её на экран
display("Baм сообщение. \n")

# Вызов функции give_me_five(), результат (5) сохраняем в переменную number
number = give_me_five()
# Выводим значение, которое вернула функция give_me_five()
print("Boт, что возвратила функция give_me_five():", number)

# Вызов функции ask_yes_no(), задаём вопрос и сохраняем ответ в переменную answer
answer = ask_yes_no("\nПожалуйста, введите 'у' или 'n': ")
# Выводим то, что пользователь ввёл
print("Спасибо, что ввели: ", answer)

