# Бесполезные кнопки
# Демонстрирует создание кнопок в графическом интерфейсе с использованием tkinter

from tkinter import *  # Импортируем все компоненты из библиотеки tkinter

# создание базового окна
root = Tk()  # Создаем главное окно приложения
root.title("Бесполезные кнопки")  # Устанавливаем заголовок окна
root.geometry("200x85")  # Устанавливаем размер окна (ширина 200, высота 85)

# внутри окна создается рамка для размещения других элементов
app = Frame(root)  # Создаем контейнер (рамку) для элементов внутри главного окна
app.grid()  # Размещаем рамку в окне с помощью сетки

# создание первой кнопки внутри рамки
bttn1 = Button(app, text="Я ничего не делаю!")  # Создаем кнопку с текстом
bttn1.grid()  # Размещаем кнопку в рамке с помощью сетки

# создание второй кнопки внутри рамки
bttn2 = Button(app)  # Создаем кнопку без текста
bttn2.grid()  # Размещаем кнопку в рамке
bttn2.configure(text="И я тоже!")  # Присваиваем кнопке текст после создания

# создание третьей кнопки внутри рамки
bttn3 = Button(app)  # Создаем кнопку без текста
bttn3.grid()  # Размещаем кнопку в рамке
bttn3["text"] = "И я!"  # Присваиваем кнопке текст через словарь атрибутов

# старт событийного цикла
root.mainloop()  # Запускаем цикл обработки событий, чтобы окно стало интерактивным
