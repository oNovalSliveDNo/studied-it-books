# Простейший GUI
# Демонстрирует создание окна с использованием библиотеки Tkinter

from tkinter import *  # Импортируем все компоненты библиотеки tkinter

# создание базового окна
root = Tk()  # Создаем главное окно приложения

# изменение окна
root.title("Простейший GUI")  # Устанавливаем заголовок окна
root.geometry("200x100")  # Устанавливаем размер окна (ширина 200, высота 100)

# старт событийного цикла
root.mainloop()  # Запускаем цикл обработки событий (необходим для взаимодействия с окном)
