# Победа
# Демонстрирует вывод сообщения на экран

from superwires import games, color  # Импортируем модуль games для работы с графикой и color для использования цветов

# Инициализируем графическое окно:
# - ширина 640 пикселей
# - высота 480 пикселей
# - fps (кадров в секунду) = 50 — определяет, как часто обновляется экран
games.init(screen_width=640, screen_height=480, fps=50)

# Загружаем изображение стены (wall.jpg) и устанавливаем его как фон
# transparent=False — значит, что изображение не будет прозрачным
wall_image = games.load_image("wall.jpg", transparent=False)
games.screen.background = wall_image  # Устанавливаем фон для игрового экрана

# Создаем сообщение "You won!" (Ты победил!) посреди экрана
# - value — текст сообщения
# - size — размер шрифта (100 — очень крупный)
# - color — цвет текста (красный)
# - x, y — координаты центра сообщения (по центру экрана)
# - lifetime — как долго сообщение будет отображаться (250 кадров)
# - after_death — что делать после исчезновения сообщения (завершить программу)
won_message = games.Message(value="You won!",  # Текст сообщения
                            size=100,  # Размер шрифта
                            color=color.red,  # Цвет текста
                            x=games.screen.width / 2,  # Положение по горизонтали — центр экрана
                            y=games.screen.height / 2,  # Положение по вертикали — центр экрана
                            lifetime=250,  # Сколько кадров сообщение будет отображаться
                            after_death=games.screen.quit)  # Завершить приложение после окончания показа

# Добавляем сообщение на экран
games.screen.add(won_message)

# Запускаем главный цикл игры — окно остаётся открытым и работает
games.screen.mainloop()
