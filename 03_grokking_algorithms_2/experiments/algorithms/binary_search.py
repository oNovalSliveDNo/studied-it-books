def binary_search(arr, item):
    """
    Осуществляет бинарный поиск элемента в отсортированном массиве.

    Параметры:
        arr (list): Отсортированный массив элементов, в котором выполняется поиск.
        item: Элемент, который необходимо найти в массиве.

    Возвращает:
        int: Индекс найденного элемента в массиве или -999, если элемент не найден.

    Примечание:
        Массив должен быть отсортирован по возрастанию для корректной работы алгоритма.
    """

    # Инициализация границ поиска:
    # low - нижняя граница (начало текущего интервала поиска)
    # high - верхняя граница (конец текущего интервала поиска)
    low = 0
    high = len(arr) - 1

    # Пока интервал поиска не сократился до нуля
    while low <= high:

        # Вычисляем средний элемент текущего интервала
        # Используется целочисленное деление для получения индекса
        mid = (low + high) // 2
        guess = arr[mid]  # Получаем значение среднего элемента

        # Если значение среднего элемента равно искомому
        if guess == item:
            return mid  # Возвращаем индекс найденного элемента

        # Если значение среднего элемента больше искомого,
        # значит искомый элемент находится в левой половине
        elif guess > item:
            high = mid - 1  # Сдвигаем верхнюю границу

        # Если значение среднего элемента меньше искомого,
        # значит искомый элемент находится в правой половине
        else:
            low = mid + 1  # Сдвигаем нижнюю границу

    # Если элемент не найден, возвращаем специальное значение
    return -999
